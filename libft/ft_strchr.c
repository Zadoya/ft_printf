#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s && s)
	{
		if (c == 0)
			return ((char *)s);
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	return (NULL);
}

#include "libft.h"

size_t	ft_front_whitespaces(const char *str, size_t i)
{
	while ((str[i] != '\0') && ((str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == '\n') || (str[i] == ' ') || (str[i] == '\r') ||
			(str[i] == '\f')))
		i++;
	return (i);
}
